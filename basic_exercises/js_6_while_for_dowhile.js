// While
document.write('<br >WHILE LOOP <br />')
var x = 1;
while (x<20) {
  document.write(x + '<br />');
  x++;
}


// dowhile
document.write('<br >DO WHILE LOOP<br />')
var y = 1;
do {
  document.write(y + '<br />');
  y++;
} while (y<20);

// for
document.write('<br >FOR LOOP<br />')
var i = 1
for (var i; i < 20; i++) {
  document.write(i + '<br />');
}
